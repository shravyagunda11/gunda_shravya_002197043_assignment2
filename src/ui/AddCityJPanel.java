public void populateTable(){
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    
    
    for(City u : cityDir.getCityList()){
    
    Object[] row = new Object[2];
    row[0]=u;
    row[1]=u.getArea();
  
    model.addRow(row);
    
    
    }}

