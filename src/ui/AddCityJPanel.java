    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField2.getText();
        int area = Integer.parseInt(jTextField1.getText());
        String area1 = jTextField1.getText();
        if(name.length()<=0){
         JOptionPane.showMessageDialog(this, "Please enter valid name");
         return;
        }
        
        if(area1.length()<=0){
                  JOptionPane.showMessageDialog(this, "Please enter valid area");
                  return;
        }
        
        if(area1.length()>0&&!area1.matches("[0-9]+")){
            JOptionPane.showMessageDialog(this, "Area can ");
            return;
        }
        
        
        
        City c = new City(name,area,new CommunityDirectory());
        if(cityDir!=null)
            cityDir.addNewcity(c);
        
        
        populateTable();
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         int row = jTable1.getSelectedRow();
        
        String name = jTable1.getValueAt(row, 0).toString();
      
        
        City obj =null;
        for(City o:cityDir.getCityList()){
            if(o.getCityName().equals(name))
                obj=o;
        }
        
        String newname = jTextField2.getText();
        int newarea= Integer.parseInt(jTextField1.getText());
        obj.setCityName(newname);
        obj.setArea(newarea);
        populateTable();
        
        for(Community c:commDir.getCommunityList()){
            if(c.getCityName().equals(name))
                c.setCityName(newname);
        }
        
         for(House h:houseDir.getHouseList()){
            if(h.getCityName().equals(name))
                h.setCityName(newname);
        }
        
         for(Hospital ho:hospDir.getHospitalList()){
            if(ho.getCit().equals(name))
                ho.setCit(newname);
        }
         
         
    }//GEN-LAST:event_jButton2ActionPerformed

