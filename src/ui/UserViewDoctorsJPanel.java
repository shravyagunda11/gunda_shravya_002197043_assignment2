    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        // TODO add your handling code here:
        String comname=null;
        if(jComboBox2.getSelectedItem()!=null)
            comname=jComboBox2.getSelectedItem().toString();
        Community comobj=null;
        for(Community c:commDir.getCommunityList()){
            if(c.getCommunityName().equals(comname)){
                comobj=c;
            }
        }
        
        Hospital hosp = comobj.getHosp().getHospitalList().get(0);
        jTextField1.setText(hosp.getHospName());
        jTextField2.setText(String.valueOf(hosp.getPhone()));
        
        populateDoctTable(hosp);
        
    }//GEN-LAST:event_jComboBox2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        
        String docname = jTable1.getValueAt(row,0).toString();
        Doctor docobj=null;
        for(Doctor d:docDir.getDoctorList()){
            if(d.getName().equals(docname))
                docobj=d;
        }
        
        Date date = jDateChooser1.getDate();
        
        //search for patient obj
        Patient patobj =  null;
        for(Patient p:patDir.getPatientList()){
            if(p.getUserName().equals(username))
                patobj=p;
        }
        
        Encounter e = new Encounter(null,date,docobj,patobj,null,null);
        encHist.addEncounter(e);
        
        
    }//GEN-LAST:event_jButton1ActionPerformed


