public void populateDoctTable(Hospital hosp){
    

  DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    
    
    for(Doctor u : hosp.getDoct().getDoctorList()){
    
    Object[] row = new Object[4];
    row[0]=u;
    row[1]=u.getExperience();
    row[2]=u.getSpecialisation();
    row[3]=u.getDegree();
  
    model.addRow(row);
    
    
    }}

