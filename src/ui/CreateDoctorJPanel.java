    private void cbCityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbCityActionPerformed
        // TODO add your handling code here:
           cbComm.removeAllItems();
          cbHouse.removeAllItems();
          cbHosp.removeAllItems();
        String city = cbCity.getSelectedItem().toString();
           City cobj=null;
        for(City ci:cityDir.getCityList() ){
           
            if(ci.getCityName().equals(city))
                cobj=ci;
        }
        
          for(Community c:cobj.getComm().getCommunityList()){
            cbComm.addItem(c.getCommunityName());
        }
    }//GEN-LAST:event_cbCityActionPerformed

    private void cbCommActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbCommActionPerformed
        // TODO add your handling code here:
                    cbHouse.removeAllItems();
                    cbHosp.removeAllItems();
        if(cbComm.getSelectedItem()!=null
                ){       
        String comm = cbComm.getSelectedItem().toString();
           Community comj=null;
        for(Community ci:commDir.getCommunityList() ){
           
            if(ci.getCommunityName().equals(comm))
                comj=ci;
        }
        
          for(House h:comj.getHouse().getHouseList()){
            cbHouse.addItem(h.getHouseno());
        }
          for(Hospital ho:comj.getHosp().getHospitalList()){
              cbHosp.addItem(ho.getHospName());
          }
        
        }
    }//GEN-LAST:event_cbCommActionPerformed


