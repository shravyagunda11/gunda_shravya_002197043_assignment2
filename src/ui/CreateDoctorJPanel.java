    private void btnCreateDoctorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateDoctorActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String userName = txtUserName.getText();
        String pass = txtPassword.getText();
        int ssn = Integer.parseInt(txtSSN.getText());
        String spec= txtSpec.getText();
        int exp = Integer.parseInt(txtExp.getText());
        String gender=cbGender.getSelectedItem().toString();
        String deg = txtDeg.getText();
        Date dob=txtDob.getDate();
        String hospname = cbHosp.getSelectedItem().toString();
        String house = cbHouse.getSelectedItem().toString();
        House hobj=null;
        //TO SEARCH FOR HOUSE

                for(House h:houseDir.getHouseList() ){
            
                        if(h.getHouseno().equals(house))
                            hobj=h;
            
                    }

        Person per = new Person(name,userName, pass, dob,hobj,ssn,gender,"doctor");
        perDir.addPerson(per);

        Doctor p = new Doctor(spec,exp,deg,hospname,name,userName, pass,dob,hobj,ssn, gender);
        docDir.addDoctor(p);

          Hospital hospj=null;
        for(Hospital ho:hospDir.getHospitalList()){
           
            if(ho.getHospName().equals(hospname))
                hospj=ho;
        }
        
        //add doctor to city's community directory and connect
   
        hospj.getDoct().addDoctor(p);
        
        JOptionPane.showMessageDialog(this, "Doctor added successfully!");
         txtName.setText("");
      txtUserName.setText("");
       txtPassword.setText("");
        txtSSN.setText("");
       txtSpec.setText("");
        txtExp.setText("");
      cbGender.setSelectedItem(null);
        txtDeg.setText("");
        txtDob.setDate(null);
        cbHosp.setSelectedItem(null);
        cbHouse.setSelectedItem(null);

    }//GEN-LAST:event_btnCreateDoctorActionPerformed

