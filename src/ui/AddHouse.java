    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String hno = jTextField2.getText();
        String stname = jTextField1.getText();
        int num =Integer.parseInt(jTextField4.getText());
        String city = jComboBox1.getSelectedItem().toString();
        String comm=jComboBox2.getSelectedItem().toString();
        String nump =jTextField4.getText();
        
        if(hno.length()<=0){
          JOptionPane.showMessageDialog(this, "Please enter valid name");
         return;}
        
        if(stname.length()<=0)
        {
          JOptionPane.showMessageDialog(this, "Please enter valid name");
         return;
        }
        
        if(nump.length()<=0){
          JOptionPane.showMessageDialog(this, "Please enter valid name");
         return;
        
        }
        
         if(nump.length()>0&&!nump.matches("[0-9]+")){
            JOptionPane.showMessageDialog(this, "Area can only be numeric");
            return;
        }
        
        
        House c = new House(hno,stname,num,city,comm);
        if(houseDir!=null)
            houseDir.addnewHouse(c);
        
      //search for Ccommunity
        Community cobj=null;
        for(Community co:commDir.getCommunityList() ){
           
            if(co.getCommunityName().equals(comm))
                cobj=co;
        }
        
        //add community to city's community directory and connect
   
        cobj.getHouse().addnewHouse(c);
        populateTable();
    }//GEN-LAST:event_jButton1ActionPerformed


