public void populateTable(){
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    
    
    for(House u : houseDir.getHouseList()){
    
    Object[] row = new Object[5];
    row[0]=u;
    row[1]=u.getStreet();
    row[2]=u.getNum();
    row[3]=u.getCityName();
    row[4]=u.getCommName();
    
  
    model.addRow(row);
    
    
    }}



