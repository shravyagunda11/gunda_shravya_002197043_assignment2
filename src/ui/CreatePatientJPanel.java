    private void btnCreatePatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreatePatientActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String userName = txtUserName.getText();
        String pass = txtPassword.getText();
        int ssn = Integer.parseInt(txtSSN.getText());
        int age = Integer.parseInt(txtAge.getText());
        String prob = txtProblem.getText();
        String gender=cbGender.getSelectedItem().toString();
         String house=cbHouse.getSelectedItem().toString();
        Date dob=txtDob.getDate();
        
          if(name.length()<=0){
         JOptionPane.showMessageDialog(this, "Please enter valid name");
         return;
        }
              if(userName.length()<=0){
         JOptionPane.showMessageDialog(this, "Please enter valid name");
         return;
        }
              if(pass.length()<=0){
         JOptionPane.showMessageDialog(this, "Please enter valid name");
         return;
        }
              
                  if(prob.length()<=0){
         JOptionPane.showMessageDialog(this, "Please enter valid name");
         return;
        }
          
            if(gender.length()<=0){
         JOptionPane.showMessageDialog(this, "Please enter valid name");
         return;
        }
            
         
          
        
        
        
        House hobj=null;
        //TO SEARCH FOR HOUSE
        if(cbHouse.getSelectedItem()!=null){
           house = cbHouse.getSelectedItem().toString();
        
        for(House h:houseDir.getHouseList() ){
            
            if(h.getHouseno().equals(house))
                hobj=h;
        
        }}
        
        
        
        Person per = new Person(name,userName, pass, dob,hobj,ssn,gender,"patient");
        perDir.addPerson(per);
        
        Patient p = new Patient(prob,name,userName, pass,dob,hobj,ssn, age, gender);
        patDir.addPatient(p);
        
        JOptionPane.showMessageDialog(this, "Patient added successfully!");
 
    
    
    }//GEN-LAST:event_btnCreatePatientActionPerformed

