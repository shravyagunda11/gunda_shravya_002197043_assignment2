    private void cbCityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbCityActionPerformed
        // TODO add your handling code here:
          cbComm.removeAllItems();
          cbHouse.removeAllItems();
        String city = cbCity.getSelectedItem().toString();
           City cobj=null;
        for(City ci:cityDir.getCityList() ){
           
            if(ci.getCityName().equals(city))
                cobj=ci;
        }
        
          for(Community c:cobj.getComm().getCommunityList()){
            cbComm.addItem(c.getCommunityName());
        }
        
    }//GEN-LAST:event_cbCityActionPerformed

